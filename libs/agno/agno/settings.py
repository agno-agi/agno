"""Grouped settings dataclasses for Agent, Team and Workflow constructor parameters.

Settings objects are input-only: the constructor unpacks them onto the flat
attributes, so `agent.session_id` etc. keep working unchanged. None means
"not set" and falls back to the flat parameter (so a settings field cannot
explicitly set a parameter to None); non-None flat defaults are recorded in
the field metadata under "flat_default". If a parameter is set both ways,
the settings object wins and a warning is logged if the values differ.

Example:
    from agno.agent import Agent
    from agno.settings import SessionSettings

    agent = Agent(session_settings=SessionSettings(session_id="my-session"))
    agent.session_id  # "my-session"
"""

