def _flat_default(settings: Any, name: str) -> Any:
    for f in fields(settings):
        if f.name == name:
            return f.metadata.get("flat_default")
    raise AttributeError(f"{type(settings).__name__} has no field {name!r}")


def _values_differ(value: Any, other: Any) -> bool:
    try:
        return bool(value != other)
    except Exception:
        return value is not other


def resolve_setting(settings: Optional[Any], name: str, flat_value: Any, flat_default: Any = MISSING) -> Any:
    """Resolve a flat constructor parameter against a grouped settings object.

    Returns the settings value when the settings object sets the field (i.e. it
    is not None), otherwise the flat value. If both are set to different values,
    the settings object wins with a warning.
    """
    if settings is None:
        return flat_value
    settings_value = getattr(settings, name)
    if settings_value is None:
        return flat_value
    if flat_default is MISSING:
        flat_default = _flat_default(settings, name)
    if _values_differ(flat_value, settings_value) and _values_differ(flat_value, flat_default):
        log_warning(
            f"Both `{name}` and `{type(settings).__name__}.{name}` provided. Using the value from `{type(settings).__name__}`."
        )
    return settings_value


def warn_unsupported_setting(settings: Optional[Any], name: str, owner: str) -> None:
    """Warn when a settings field is set but not supported by the receiving class."""
    if settings is not None and getattr(settings, name) is not None:
        log_warning(f"`{type(settings).__name__}.{name}` is not supported by {owner} and will be ignored.")
