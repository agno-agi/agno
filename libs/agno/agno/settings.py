@dataclass
class ModelSettings:
    """Model selection and fallback behavior."""

    # Model to use
    model: Optional[Union[Model, str]] = None
    # Fallback models tried when the primary model fails
    fallback_models: Optional[List[Union[Model, str]]] = None
    # Fallback configuration for model failures (advanced: error-specific routing)
    fallback_config: Optional[FallbackConfig] = None


@dataclass
class SessionSettings:
    """Session identity, state and past-session search."""

    # Default session_id (autogenerated if not set)
    session_id: Optional[str] = None
    # Default session state (stored in the database to persist across runs)
    session_state: Optional[Dict[str, Any]] = None
    # Set to True to add the session_state to the context
    add_session_state_to_context: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # Set to True to give the model tools to update the session_state dynamically
    enable_agentic_state: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # Set to True to overwrite the stored session_state with the session_state provided in the run. Default behaviour merges the current session state with the session state in the db
    overwrite_db_session_state: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, cache the current session in memory for faster access
    cache_session: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, add a tool to search past sessions
    search_past_sessions: Optional[bool] = field(default=None, metadata={"flat_default": False})
    num_past_sessions_to_search: Optional[int] = None
    num_past_session_runs_in_search: Optional[int] = None
    # If True, create/update session summaries at the end of runs
    enable_session_summaries: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, add session summaries to the context
    add_session_summary_to_context: Optional[bool] = None
    # Session summary manager
    session_summary_manager: Optional[SessionSummaryManager] = None


@dataclass
class ContextSettings:
    """Context building: datetime/location, formatting and message roles."""

    # If markdown=True, add instructions to format the output using markdown
    markdown: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, add the name to the instructions
    add_name_to_context: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, add the current datetime to the instructions
    add_datetime_to_context: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, add the current location to the instructions
    add_location_to_context: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # Custom datetime format string (e.g. "%Y-%m-%d %H:%M:%S")
    datetime_format: Optional[str] = None
    # Custom timezone for datetime instructions following the TZ Database format (e.g. "Etc/UTC")
    timezone_identifier: Optional[str] = None
    # If True, resolve session_state, dependencies, and metadata in the user and system messages
    resolve_in_context: Optional[bool] = field(default=None, metadata={"flat_default": True})
    # Additional context added to the end of the system message
    additional_context: Optional[str] = None
    # A list of extra messages added after the system message and before the user message
    additional_input: Optional[List[Union[str, Dict, BaseModel, Message]]] = None
    # Dependencies available for tools and prompt functions
    dependencies: Optional[Dict[str, Any]] = None
    # If True, add the dependencies to the user prompt
    add_dependencies_to_context: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # If True, wrap instructions in <instructions> tags
    use_instruction_tags: Optional[bool] = field(default=None, metadata={"flat_default": False})
    # Role for the system message
    system_message_role: Optional[str] = field(default=None, metadata={"flat_default": "system"})
    # Role for the user message (Agent only)
    user_message_role: Optional[str] = field(default=None, metadata={"flat_default": "user"})
    # Set to False to skip context building (Agent only)
    build_context: Optional[bool] = field(default=None, metadata={"flat_default": True})
    # Set to False to skip building the user context (Agent only)
    build_user_context: Optional[bool] = field(default=None, metadata={"flat_default": True})


